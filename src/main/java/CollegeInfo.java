    private boolean isFormValid(){
        if(idTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! fill up College id in the form to save information.");
            return false;
        }
        else if(collegeNameTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! fill up College Name in the form to save information.");
            return false;
        }
        else if(collegeCategoryCB.getSelectedItem().toString().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! select college category in the form to save information.");
            return false;
        }
        else if(affilationCB.getSelectedItem().toString().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! select affilation option in the form to save information.");
            return false;
        }
        else if(buttonGroup1.isSelected(null)){
            JOptionPane.showMessageDialog(rootPane, "Please! select either yes or no for scholarship field in the form to save information.");
            return false;
        }
        else if(feeTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! fill up College fee in the form to save information.");
            return false;
        }
        
        else if(addressTA.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! fill up College address in the form to save information.");
            return false;
        }
        else if(contactTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! fill up College contact in the form to save information.");
            return false;
        }
        
        if(!(scienceCB.isSelected() || managementCB.isSelected() || artCB.isSelected() || itCB.isSelected() || bbaCB.isSelected() || otherCB.isSelected())){
            JOptionPane.showMessageDialog(rootPane, "Please! select stream option in the form to save information.");
            return false;
        }
        
        if(!(idTF.getText().isEmpty())){
            for (int row = 0; row < collegeIsTable.getRowCount(); row++){
                if(collegeIsTable.getValueAt(row, 0).toString().equals(idTF.getText())){
                    JOptionPane.showMessageDialog(rootPane, "The college with given College ID is already present in the Information System. \nPlease! Insert another College ID to insert details.");  
                    return false;
                }
            }
        }
        
        
        if(!(feeTF.getText().isEmpty())){
            try{
                Integer.parseInt(feeTF.getText());
            }
            catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Invalid Fee!! \nPlease! Enter the Fee in number format.");
                return false;
            }
        }
        
        return true;
    }
    
