    // Get sorted feeList to be used in binary search.
    public int[] sort(int[]a){
        for(int i = 0; i< a.length-1 ; i++ ){
            int minPos = minimumPosition(a, i);
            swap(a, minPos, i);
        }
        return a;
    }
    private int minimumPosition(int[] a, int from){
        int minPos = from;
        for(int j = from + 1; j < a.length; j++){
            if (a[j]< a[minPos]){
                minPos = j;
            }
        }
        return minPos;
    }
    private void swap(int[] a, int i, int j){
        int temp = a[i];
        a[i] = a[j];
        a[j] = temp;
    }
    
    // this is binarySearch method for listing colleges according to fee
    public int binSearch(int[] list, int target){
        int lowPosn = 0;
        int highPosn = list.length -1;
        while(lowPosn <= highPosn){
            int midPosn = (lowPosn + highPosn)/2;
            if(list[midPosn] == target){
                return midPosn;
            }
            else if(list[midPosn] > target){
                highPosn = midPosn -1;
            }
            else{
                lowPosn = midPosn +1;
            }
        }
        
        return -1;
    }
    
    
    
