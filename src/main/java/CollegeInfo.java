    private void searchByFeeBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchByFeeBActionPerformed
       //validation for search fee text
        int target = 0;
        if(searchFeeTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please! Enter fee details to search College.");
            return;
        }
        try{
            target = Integer.parseInt(searchFeeTF.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Invalid fee inserted.");
            return;
        }
        
        // Get sorted feeList.
        int[] fee = new int[collegeIsTable.getRowCount()];
        for (int row = 0; row < collegeIsTable.getRowCount(); row++){
            fee[row] = Integer.parseInt(collegeIsTable.getValueAt(row, 6).toString());
        }
        sort(fee);
        
        
        int result = binSearch(fee, target);
        if(result == -1){
            JOptionPane.showMessageDialog(rootPane, "The college with given fee is not present in the College Information System."); 
        }
        else{
            for(int row = 0; row<collegeIsTable.getRowCount(); row++) {
                if(fee[result] == Integer.parseInt(collegeIsTable.getValueAt(row, 6).toString())) {
                    JOptionPane.showMessageDialog(rootPane, "The details of college with fee \"" +target+"\" is given below: "+ "\n\nCollege ID : " +  collegeIsTable.getValueAt(row, 0) + "\nCollege Name: " + collegeIsTable.getValueAt(row, 1)+ "\nCategory: " + collegeIsTable.getValueAt(row, 2) + "\nAffiliation: "+ collegeIsTable.getValueAt(row, 3)+ "\nStream: " + collegeIsTable.getValueAt(row, 4)+ "\nScholarship: "+ collegeIsTable.getValueAt(row, 5)+ "\nCollege Fee: Rs. " + collegeIsTable.getValueAt(row, 6) + "\nAddress: " + collegeIsTable.getValueAt(row, 7) + "\nContact: " + collegeIsTable.getValueAt(row, 8));
                    break;
                }
            }
        }
        searchFeeTF.setText("");
    }//GEN-LAST:event_searchByFeeBActionPerformed

